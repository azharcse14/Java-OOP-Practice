package com.azhar.MathClass;

public class MathClass {
    public static void main(String[] args) {
        int a = Math.abs(-9);
        System.out.println(a);

        System.out.println(Math.abs(77));
        System.out.println(Math.max(44, 33));
        System.out.println(Math.log(33.33));
        System.out.println(Math.ceil(55.3));
        System.out.println(Math.floor(44.99));
        System.out.println(Math.random());
    }
}
